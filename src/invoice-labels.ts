/**
 * Get native labels for a given language code.
 * Always returns native translations regardless of PDF font support.
 * Use this for HTML live preview.
 * Falls back to English if the language is not found.
 */
// Language code to locale mapping for date formatting
const LOCALE_MAP: Record<string, string> = {
  en: "en-US", hi: "hi-IN", es: "es-ES", fr: "fr-FR", de: "de-DE",
  pt: "pt-BR", it: "it-IT", nl: "nl-NL", tr: "tr-TR", ar: "ar-SA",
  zh: "zh-CN", "zh-TW": "zh-TW", ja: "ja-JP", ko: "ko-KR", ru: "ru-RU",
  bn: "bn-BD", ta: "ta-IN", ur: "ur-PK", ms: "ms-MY", id: "id-ID",
  th: "th-TH", vi: "vi-VN", fil: "fil-PH", pl: "pl-PL", sv: "sv-SE",
  uk: "uk-UA", ro: "ro-RO", hu: "hu-HU", el: "el-GR", he: "he-IL",
  fa: "fa-IR", sw: "sw-TZ",
};

export function getLocale(langCode: string): string {
  return LOCALE_MAP[langCode] || "en-US";
}

export function getLabels(langCode: string): InvoiceLabels {
  const entry = LABELS[langCode];
  if (!entry) return LABELS.en;
  return entry;
}

/**
 * Get PDF-safe labels for a given language code.
 * Returns English labels for non-Latin-script languages (Helvetica limitation).
 * Use this for PDF generation.
 */
export function getPdfLabels(langCode: string): InvoiceLabels {
  const entry = LABELS[langCode];
  if (!entry || entry.latinFallback) return LABELS.en;
  return entry;
}

/**
 * Check if a language requires English fallback in the PDF.
 */
export function needsLatinFallback(langCode: string): boolean {
  const entry = LABELS[langCode];
  return !entry || entry.latinFallback;
}
